export function getRandomWordPair(): WordPair {
  return WORD_PAIRS[Math.floor(Math.random() * WORD_PAIRS.length)];
}

export function getUnusedWordPair(usedWordPairs: string[]): WordPair {
  // Get available word pairs that haven't been used
  const availablePairs = WORD_PAIRS.filter(pair => {
    const pairId = `${pair.main}-${pair.imposter}`;
    return !usedWordPairs.includes(pairId);
  });
  
  // If all pairs have been used, reset and use any pair
  if (availablePairs.length === 0) {
    return WORD_PAIRS[Math.floor(Math.random() * WORD_PAIRS.length)];
  }
  
  // Return random unused pair
  return availablePairs[Math.floor(Math.random() * availablePairs.length)];
}